    builder = load_builder()
    builder.BASE = BASE
    builder.SLUG = SLUG
    builder.MD_PATH = BASE / f"{SLUG}.md"
    builder.HTML_PATH = BASE / f"{SLUG}.html"
    builder.DOCX_PATH = BASE / f"{SLUG}.docx"
    builder.PDF_PATH = BASE / f"{SLUG}.pdf"
    builder.REVIEW_PATH = BASE / "review-notes.md"
    builder.REPORT = REPORT
    builder.main()


