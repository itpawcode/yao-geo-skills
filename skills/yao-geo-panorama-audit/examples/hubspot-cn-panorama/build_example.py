def load_builder():
    spec = importlib.util.spec_from_file_location("panorama_builder", SOURCE_BUILDER)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"无法加载基础生成器：{SOURCE_BUILDER}")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


